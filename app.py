def soma(a, b):
    """
    Função que recebe dois números e retorna a soma deles.

    Parâmetros:
    a (int, float): O primeiro número.
    b (int, float): O segundo número.

    Retorna:
    int, float: A soma de a e b.
    """
    return a + b